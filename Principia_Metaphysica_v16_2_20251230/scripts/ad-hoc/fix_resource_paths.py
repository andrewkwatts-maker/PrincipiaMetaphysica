import os

# Files in Website folder that need resource path updates
website_files = [
    'appendices.html', 'beginners-guide.html', 'formulas.html',
    'foundations.html', 'paper.html', 'parameters.html',
    'philosophical-implications.html', 'references.html',
    'sections.html', 'simulations.html', 'visualization-index.html'
]

for filename in website_files:
    filepath = f'h:\\Github\\PrincipiaMetaphysica\\Website\\{filename}'

    if not os.path.exists(filepath):
        print(f"Skipping {filename} - not found")
        continue

    print(f"Processing {filename}...")

    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    original_content = content
    changes = []

    # Update CSS references: href="css/ -> href="../css/
    if 'href="css/' in content:
        content = content.replace('href="css/', 'href="../css/')
        changes.append("CSS links")

    # Update JS references: src="js/ -> src="../js/
    if 'src="js/' in content:
        content = content.replace('src="js/', 'src="../js/')
        changes.append("JS scripts")

    # Update AutoGenerated references: e.g., AutoGenerated/ -> ../AutoGenerated/
    if '"AutoGenerated/' in content:
        content = content.replace('"AutoGenerated/', '"../AutoGenerated/')
        changes.append("AutoGenerated paths")

    # Update data/ references
    if 'data/' in content and '"data/' in content:
        content = content.replace('"data/', '"../data/')
        changes.append("data paths")

    # Update foundations/ references
    if 'href="foundations/' in content:
        content = content.replace('href="foundations/', 'href="../foundations/')
        changes.append("foundations links")

    # Update sections/ references
    if 'href="sections/' in content:
        content = content.replace('href="sections/', 'href="../sections/')
        changes.append("sections links")

    if content != original_content:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"  Updated: {', '.join(changes)}")
    else:
        print(f"  No changes needed")

print("\nResource path updates complete!")
