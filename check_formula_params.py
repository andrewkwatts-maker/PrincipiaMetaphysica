import json

data = json.load(open('AutoGenerated/theory_output.json', encoding='utf-8'))
formulas = data.get('formulas', {})

for fid, formula in list(formulas.items())[:30]:
    inp = formula.get('input_params', [])
    outp = formula.get('output_params', [])
    if inp or outp:
        print(f'{fid}: input={len(inp)}, output={len(outp)}')
        if inp:
            print(f'  Sample input: {inp[0]}')
        if outp:
            print(f'  Sample output: {outp[0]}')
        break
