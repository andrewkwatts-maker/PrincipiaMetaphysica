#!/usr/bin/env python3
import json
from pathlib import Path

autogen = Path(r"H:\Github\PrincipiaMetaphysica\AutoGenerated")
files = ['formulas.json', 'theory_output.json', 'parameters.json', 'sections.json']

total = 0
for fname in files:
    fpath = autogen / fname
    with open(fpath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    text = json.dumps(data)
    count = text.count('27D')
    print(f'{fname}: {count} instances of "27D"')
    total += count

print(f'\nTOTAL: {total} remaining "27D" instances')
print(f'Status: {"PUBLICATION READY ✅" if total == 0 else "NEEDS REVIEW ⚠️"}')
