def apply_replacements(text, is_latex=False, is_description=False):
    """Apply replacement patterns to text"""
    if not isinstance(text, str):
        return text

    result = text

    # Apply LaTeX-specific replacements first if applicable
    if is_latex:
        for old, new in LATEX_REPLACEMENTS.items():
            result = re.sub(old, new, result)

    # Apply standard replacements (order matters - longest first)
    sorted_replacements = sorted(REPLACEMENTS.items(), key=lambda x: len(x[0]), reverse=True)
    for old, new in sorted_replacements:
        # Use simple string replacement for all non-regex patterns
        if old.startswith(r"\\") or old.startswith("r\""):
            # This is a regex pattern
            result = re.sub(old, new, result)
        else:
            # Simple string replacement (safer)
            result = result.replace(old, new)

    # Apply description enhancements if this is a description field
    if is_description:
        for old, new in DESCRIPTION_ENHANCEMENTS.items():
            # Only replace if not already enhanced
            if old in result and new not in result:
                result = result.replace(old, new)

    return result


