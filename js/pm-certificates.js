/* ============================================================================
 * pm-certificates.js
 *
 * Sprint T2 task #9 (carryover from Sprint 3.9) — Gate-card trio extension.
 *
 * Decorates each rendered gate card on Pages/certificates.html with the
 * three-column Arithma + EML + Computed-value trio first introduced for
 * formulas in Sprint 3.6, and wires a "Trace to b₃" button into
 * pm-b3-tracer.js (Sprint 6.5).
 *
 * Mirrors the Sprint 3.6 widget pattern (pm-tv-panel CSS from
 * css/pm-formula-card.css):
 *
 *   ┌──────────────┬──────────────┬───────────────┐
 *   │ Arithma      │ EML          │ Computed       │
 *   │  72 × 3      │ ops.mul(…)   │ 216 (LOCKED)   │
 *   └──────────────┴──────────────┴───────────────┘
 *   [Trace to b₃] [b₃-rooted ✓]
 *
 * The trio is hydrated **after** the inline renderGates() inside
 * certificates.html runs, so we do not need to modify the existing
 * gate-card renderer; we just patch every card in place using its
 * cert-number ("G14") to look up the matching gate + formula record.
 *
 * Inputs (all loaded with fetch, with package/dev path fallbacks):
 *   - AutoGenerated/GATES_CERTIFICATES.json
 *   - AutoGenerated/formulas.json
 *
 * Each gate g{NN}_… has a sibling formula entry g{NN}-… in formulas.json
 * containing the EML tree (eml_tree_str) and an Arithma-side LaTeX (latex).
 *
 * Forward-compat: if a gate later carries the migrated Sprint 2.8 fields
 *   - arithma_latex
 *   - eml_tree_compact
 *   - value
 * we will prefer those over the legacy fallbacks transparently.
 *
 * Copyright (c) 2026 Andrew Keith Watts.
 * ============================================================================ */
(function () {
    'use strict';

    // ── Configuration ──────────────────────────────────────────────────────
    const CANDIDATE_PREFIXES = [
        '../AutoGenerated/',
        './AutoGenerated/',
        '/AutoGenerated/',
        'AutoGenerated/',
        '../simulations/AutoGenerated/'
    ];

    // ── State ──────────────────────────────────────────────────────────────
    let gatesIndex = null;     // { 1: certRecord, 2: certRecord, ... }
    let formulasIndex = null;  // { id: formulaRecord }
    let formulaByGate = null;  // { 1: formulaRecord, 2: formulaRecord, ... }
    let loadPromise = null;

    // ── Data loading ───────────────────────────────────────────────────────
    async function fetchFirst(filename) {
        let lastErr = null;
        for (const prefix of CANDIDATE_PREFIXES) {
            try {
                const resp = await fetch(prefix + filename, { cache: 'no-store' });
                if (resp.ok) return await resp.json();
                lastErr = new Error('HTTP ' + resp.status + ' for ' + prefix + filename);
            } catch (e) {
                lastErr = e;
            }
        }
        throw lastErr || new Error('Could not load ' + filename);
    }

    async function loadAll() {
        if (loadPromise) return loadPromise;
        loadPromise = (async () => {
            const [gatesDoc, formulasDoc] = await Promise.all([
                fetchFirst('GATES_CERTIFICATES.json'),
                fetchFirst('formulas.json')
            ]);

            const certs = (gatesDoc && (gatesDoc.certificates || gatesDoc.gates)) || [];
            gatesIndex = {};
            certs.forEach(c => {
                if (c && c.gate_id != null) gatesIndex[c.gate_id] = c;
            });

            formulasIndex = (formulasDoc && formulasDoc.formulas) || {};

            // Build gate-number → formula record map using g{NN}- prefix lookup
            formulaByGate = {};
            for (let n = 1; n <= 72; n++) {
                const padded = String(n).padStart(2, '0');
                const exact = formulasIndex['g' + padded + '-'];
                if (exact) {
                    formulaByGate[n] = exact;
                    continue;
                }
                // Scan for matching prefix
                const prefix = 'g' + padded + '-';
                const altPrefix = 'g' + n + '-';
                const match = Object.keys(formulasIndex).find(k =>
                    k.startsWith(prefix) || k.startsWith(altPrefix)
                );
                if (match) formulaByGate[n] = formulasIndex[match];
            }

            return { gatesIndex, formulasIndex, formulaByGate };
        })();
        return loadPromise;
    }

    // ── Helpers ────────────────────────────────────────────────────────────
    function escapeHtml(s) {
        if (s === null || s === undefined) return '';
        return String(s)
            .replace(/&/g, '&amp;')
            .replace(/</g, '&lt;')
            .replace(/>/g, '&gt;')
            .replace(/"/g, '&quot;')
            .replace(/'/g, '&#39;');
    }

    /** Extract numeric gate ID from the "G14" cert-number badge. */
    function gateIdFromCard(card) {
        const badge = card.querySelector('.cert-number');
        if (!badge) return null;
        const txt = (badge.textContent || '').trim();
        // Accept "G14", "G07", "C42"
        const m = txt.match(/^[GC](\d+)$/i);
        if (!m) return null;
        return parseInt(m[1], 10);
    }

    /** Resolve the Arithma representation for a gate. */
    function pickArithma(cert, formula) {
        // Prefer migrated Sprint 2.8 field, then formula.latex, then gate.formula text.
        if (cert && cert.arithma_latex) return { latex: cert.arithma_latex, text: '' };
        if (formula && formula.arithma_latex) return { latex: formula.arithma_latex, text: '' };
        if (formula && formula.latex && !/^\s*\\text\{/.test(formula.latex)) {
            return { latex: formula.latex, text: '' };
        }
        // Fallback to the symbolic string on the gate record.
        if (cert && cert.formula) return { latex: '', text: cert.formula };
        if (formula && formula.plain_text) return { latex: '', text: formula.plain_text };
        return { latex: '', text: '' };
    }

    /** Resolve the EML representation. */
    function pickEml(cert, formula) {
        if (cert && cert.eml_tree_compact && cert.eml_tree_compact.x) {
            return { latex: cert.eml_tree_compact.x, text: '' };
        }
        if (formula && formula.eml_tree_compact && formula.eml_tree_compact.x) {
            return { latex: formula.eml_tree_compact.x, text: '' };
        }
        if (formula && formula.eml_latex) return { latex: formula.eml_latex, text: '' };
        if (formula && formula.eml_tree_str) return { latex: '', text: formula.eml_tree_str };
        return { latex: '', text: '' };
    }

    /** Resolve the computed value to display. */
    function pickValue(cert, formula) {
        // Prefer migrated Sprint 2.8 numeric 'value' field
        if (cert && cert.value !== undefined && cert.value !== null) {
            return formatValue(cert.value, cert);
        }
        if (formula && formula.value !== undefined && formula.value !== null) {
            return formatValue(formula.value, cert);
        }
        // Fall back to gate.result (string number or status)
        const res = cert ? cert.result : null;
        if (res === null || res === undefined || res === '' || res === 'N/A') {
            // Use the verification status (LOCKED / VERIFIED / MATHEMATICAL) as label.
            const status = (cert && cert.verification_status) || 'LOCKED';
            return { number: '', label: status, units: '' };
        }
        // Try to coerce to number
        const asNum = Number(res);
        if (!Number.isNaN(asNum) && /^[\s\d.eE+\-]+$/.test(String(res))) {
            return formatValue(asNum, cert);
        }
        return { number: '', label: String(res), units: '' };
    }

    function formatValue(v, cert) {
        let numStr;
        if (typeof v === 'number' && Number.isFinite(v)) {
            const absv = Math.abs(v);
            if (absv === 0) {
                numStr = '0';
            } else if (absv < 1e-3 || absv >= 1e5) {
                numStr = v.toExponential(6);
            } else {
                numStr = v.toPrecision(8);
            }
        } else {
            numStr = String(v);
        }
        const label = (cert && cert.verification_status) || 'LOCKED';
        return { number: numStr, label, units: '' };
    }

    /** Decide if the gate's chain is b3-rooted (best-effort from local data). */
    function isB3Rooted(formula) {
        if (!formula) return false;
        if (formula.b3_rooted === true || formula.b3Rooted === true) return true;
        const eml = formula.eml_tree_str || '';
        // op-token chain referencing eml_vec('b3') or a raw 24 leaf indicates a b3 root path
        if (/eml_vec\(['"]b3['"]\)/.test(eml)) return true;
        if (/\bb3\b/.test(eml)) return true;
        return false;
    }

    // ── Trio HTML builder ──────────────────────────────────────────────────
    function buildTrio(cert, formula, gateId) {
        const arithma = pickArithma(cert, formula);
        const eml = pickEml(cert, formula);
        const value = pickValue(cert, formula);
        const rooted = isB3Rooted(formula);
        const formulaId = (formula && formula.id) || ('g' + String(gateId).padStart(2, '0'));

        const arithmaBody = arithma.latex
            ? `\\(${arithma.latex}\\)`
            : (arithma.text
                ? `<code class="pm-cert-trio__code">${escapeHtml(arithma.text)}</code>`
                : '<div class="pm-tv-panel__empty">No Arithma</div>');

        const emlBody = eml.latex
            ? `\\(${eml.latex}\\)`
            : (eml.text
                ? `<code class="pm-cert-trio__code pm-cert-trio__code--eml">${escapeHtml(eml.text)}</code>`
                : '<div class="pm-tv-panel__empty">No EML</div>');

        const valueBody = value.number
            ? `<span class="pm-tv-value__number">${escapeHtml(value.number)}</span>`
              + (value.units ? `<span class="pm-tv-value__units">${escapeHtml(value.units)}</span>` : '')
              + (value.label ? `<span class="pm-tv-value__rel">${escapeHtml(value.label)}</span>` : '')
            : `<span class="pm-tv-value__number pm-tv-value__number--label">${escapeHtml(value.label || 'LOCKED')}</span>`;

        const rootedBadge = rooted
            ? '<span class="pm-cert-trio__rooted" title="Dependency chain terminates at b₃ = 24">b<sub>3</sub>-rooted &#x2713;</span>'
            : '<span class="pm-cert-trio__rooted pm-cert-trio__rooted--no" title="Chain not yet b₃-rooted">b<sub>3</sub>-rooted &#x2717;</span>';

        return [
            '<div class="pm-cert-trio" data-formula-id="', escapeHtml(formulaId), '">',
                '<div class="pm-cert-trio__head">',
                    '<span class="pm-cert-trio__label">Arithma + EML + Computed</span>',
                    rootedBadge,
                '</div>',
                '<div class="pm-triple-view">',
                    '<div class="pm-tv-panel pm-tv-panel--arithma">',
                        '<div class="pm-tv-panel__label"><span>Arithma</span><span class="pm-tv-tag">LaTeX</span></div>',
                        '<div class="pm-tv-panel__body">', arithmaBody, '</div>',
                    '</div>',
                    '<div class="pm-tv-panel pm-tv-panel--eml">',
                        '<div class="pm-tv-panel__label"><span>EML</span><span class="pm-tv-tag">op-tree</span></div>',
                        '<div class="pm-tv-panel__body">', emlBody, '</div>',
                    '</div>',
                    '<div class="pm-tv-panel pm-tv-panel--value">',
                        '<div class="pm-tv-panel__label"><span>Computed</span><span class="pm-tv-tag">value</span></div>',
                        '<div class="pm-tv-panel__body">', valueBody, '</div>',
                    '</div>',
                '</div>',
                '<div class="pm-cert-trio__actions">',
                    '<button type="button"',
                        ' class="pm-cert-trio__btn pm-cert-trio__btn--primary"',
                        ' data-action="trace-b3"',
                        ' data-formula-id="', escapeHtml(formulaId), '"',
                        ' title="Open the dependency graph and walk this gate down to b₃">',
                        '&#x21B3; Trace to b<sub>3</sub>',
                    '</button>',
                '</div>',
            '</div>'
        ].join('');
    }

    // ── Card hydration ─────────────────────────────────────────────────────
    function injectTrioIntoCard(card) {
        if (!card || card.dataset.pmCertTrio === '1') return false;
        const gid = gateIdFromCard(card);
        if (gid == null) return false;
        const cert = gatesIndex[gid];
        const formula = formulaByGate[gid];
        if (!cert && !formula) return false;

        // Avoid duplicate insertion if re-rendered
        const existing = card.querySelector('.pm-cert-trio');
        if (existing) existing.remove();

        const html = buildTrio(cert, formula, gid);
        const body = card.querySelector('.cert-card-body');
        if (!body) return false;

        // Insert the trio just before .cert-status so it sits between
        // the gate formula and the LOCKED/Wolfram badges.
        const status = body.querySelector('.cert-status');
        const wrap = document.createElement('div');
        wrap.innerHTML = html;
        const trioEl = wrap.firstChild;
        if (status) {
            body.insertBefore(trioEl, status);
        } else {
            body.appendChild(trioEl);
        }
        card.dataset.pmCertTrio = '1';

        // Re-typeset MathJax for the new content
        if (window.MathJax && window.MathJax.typesetPromise) {
            window.MathJax.typesetPromise([trioEl]).catch(() => {});
        }
        return true;
    }

    function hydrateAll(root) {
        if (!root) root = document;
        const cards = root.querySelectorAll('.cert-card');
        let n = 0;
        cards.forEach(card => {
            if (injectTrioIntoCard(card)) n++;
        });
        return n;
    }

    // ── Public API ─────────────────────────────────────────────────────────
    const PMCertificates = {
        load: loadAll,
        hydrateAll: async function (root) {
            try {
                await loadAll();
            } catch (err) {
                console.error('[pm-certificates] failed to load data:', err);
                return 0;
            }
            return hydrateAll(root);
        },
        hydrateCard: async function (card) {
            try {
                await loadAll();
            } catch (err) {
                console.error('[pm-certificates] failed to load data:', err);
                return false;
            }
            return injectTrioIntoCard(card);
        }
    };

    window.PMCertificates = PMCertificates;

    // ── Auto-hydration ─────────────────────────────────────────────────────
    // Watch the cert-grid for content changes — the inline renderer in
    // certificates.html re-creates cards whenever the user clicks a domain
    // filter, so we need to re-inject the trio after each render.
    function setupAutoHydration() {
        const grid = document.getElementById('cert-grid');
        if (!grid) return;

        // Initial pass (in case cards are already present)
        PMCertificates.hydrateAll(grid);

        // Re-hydrate after any mutation
        const obs = new MutationObserver(() => {
            // Debounce — coalesce burst mutations
            if (obs._pending) return;
            obs._pending = true;
            requestAnimationFrame(() => {
                obs._pending = false;
                PMCertificates.hydrateAll(grid);
            });
        });
        obs.observe(grid, { childList: true, subtree: false });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', setupAutoHydration);
    } else {
        setupAutoHydration();
    }

    console.log('[PM] certificates trio extension initialised');
})();
