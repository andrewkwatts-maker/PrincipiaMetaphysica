"""
eml_param_crosscheck.py — Cross-check all eml_description strings in parameters.json.

For every parameter that has an eml_description field, parses and evaluates the
EML expression using EMLEvaluator and compares the result to the registered value.

Results are printed as a table and written to AutoGenerated/eml_crosscheck.json.

Usage:
    python scripts/eml_param_crosscheck.py
    python scripts/eml_param_crosscheck.py --verbose
    python scripts/eml_param_crosscheck.py --tolerance 0.05  # 5% rel tolerance
    python scripts/eml_param_crosscheck.py --only-fails
"""
from __future__ import annotations

import argparse
import json
import math
import sys
from pathlib import Path
from typing import Any, Optional

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

try:
    from eml_math.evaluator import EMLEvaluator
except ImportError as e:
    print(f"ERROR: cannot import eml_math.evaluator — {e}")
    print("Install or update eml-math: pip install -e H:/Github/EML-Math")
    sys.exit(1)

PARAMS_JSON = ROOT / "AutoGenerated" / "parameters.json"
OUTPUT_JSON = ROOT / "AutoGenerated" / "eml_crosscheck.json"

DEFAULT_TOLERANCE = 0.001  # 0.1% relative tolerance


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _load_parameters(path: Path) -> dict[str, Any]:
    """Load parameters.json and return the flat {dotted_path: entry} dict.

    parameters.json has the structure::
        {"version": ..., "parameters": {"ckm.V_cb": {...}, ...}}
    so we just return the inner "parameters" dict directly.
    """
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    return data.get("parameters", data)


def _get_eml_description(entry: dict[str, Any]) -> str:
    """Return eml_description from an entry (top-level or inside metadata)."""
    desc = entry.get("eml_description", "")
    if not desc:
        desc = (entry.get("metadata") or {}).get("eml_description", "")
    return desc or ""


def _build_context(params: dict[str, Any]) -> dict[str, float]:
    """Build {name: float_value} context for EMLEvaluator from all params.

    Adds three aliases for every parameter path ``a.b.c``:
    - full path:       ``a.b.c``
    - last component:  ``c``            (e.g. 'lambda_wolfenstein')
    - last two:        ``b.c``          (e.g. 'ckm.lambda_wolfenstein')

    This covers the variety of reference styles used in eml_description strings.
    When aliases collide, the longest (most specific) path wins.
    """
    ctx: dict[str, float] = {}
    # Two passes: short aliases first, then full paths overwrite on collision.
    for path, entry in params.items():
        val = entry.get("value")
        if val is None:
            continue
        try:
            f = float(val)
        except (TypeError, ValueError):
            continue
        if not math.isfinite(f):
            continue

        parts = path.split(".")
        # Short alias: last component only
        ctx.setdefault(parts[-1], f)
        # Medium alias: last two components
        if len(parts) >= 2:
            ctx.setdefault(".".join(parts[-2:]), f)

    # Full paths overwrite (highest priority)
    for path, entry in params.items():
        val = entry.get("value")
        if val is None:
            continue
        try:
            f = float(val)
        except (TypeError, ValueError):
            continue
        if not math.isfinite(f):
            continue
        ctx[path] = f

    return ctx


def _rel_error(evaluated: float, registered: float) -> Optional[float]:
    """Relative error between evaluated and registered values."""
    if registered == 0.0:
        return abs(evaluated - registered)
    return abs(evaluated - registered) / abs(registered)


# ---------------------------------------------------------------------------
# Main check
# ---------------------------------------------------------------------------

def run_crosscheck(
    tolerance: float = DEFAULT_TOLERANCE,
    verbose: bool = False,
    only_fails: bool = False,
) -> dict[str, Any]:
    params = _load_parameters(PARAMS_JSON)
    context = _build_context(params)

    evaluator = EMLEvaluator(context, strict=False)

    results: list[dict[str, Any]] = []
    n_agree = n_disagree = n_skip = n_no_value = 0

    for path, entry in sorted(params.items()):
        eml_desc = _get_eml_description(entry)
        if not eml_desc:
            n_skip += 1
            if verbose:
                results.append({"path": path, "status": "SKIP", "reason": "no eml_description"})
            continue

        registered_val = entry.get("value")
        if registered_val is None:
            n_no_value += 1
            results.append({
                "path": path,
                "status": "SKIP",
                "reason": "no registered value",
                "eml_description": eml_desc,
            })
            continue

        try:
            registered_float = float(registered_val)
        except (TypeError, ValueError):
            n_no_value += 1
            results.append({
                "path": path,
                "status": "SKIP",
                "reason": f"non-numeric value: {registered_val!r}",
                "eml_description": eml_desc,
            })
            continue

        err_msg: Optional[str] = None
        try:
            evaluated = evaluator.eval(eml_desc)
        except Exception as exc:
            evaluated = None
            err_msg = str(exc)

        if evaluated is None:
            n_skip += 1
            rec = {
                "path": path,
                "status": "ERROR",
                "reason": err_msg or "eval raised exception",
                "registered": registered_float,
                "eml_description": eml_desc,
            }
            results.append(rec)
            if verbose:
                print(f"  ERROR  {path}: {err_msg}")
            continue

        if not math.isfinite(evaluated) or not math.isfinite(registered_float):
            n_skip += 1
            results.append({
                "path": path,
                "status": "SKIP",
                "reason": "non-finite value",
                "evaluated": evaluated,
                "registered": registered_float,
                "eml_description": eml_desc,
            })
            continue

        rel_err = _rel_error(evaluated, registered_float)
        agree = rel_err is not None and rel_err <= tolerance

        rec: dict[str, Any] = {
            "path": path,
            "status": "AGREE" if agree else "DISAGREE",
            "evaluated": evaluated,
            "registered": registered_float,
            "rel_error": rel_err,
            "eml_description": eml_desc,
        }
        if evaluator.missing_refs:
            rec["missing_refs"] = list(evaluator.missing_refs)
            evaluator.missing_refs.clear()

        results.append(rec)

        if agree:
            n_agree += 1
            if verbose and not only_fails:
                print(f"  AGREE  {path}  (eval={evaluated:.6g}  reg={registered_float:.6g})")
        else:
            n_disagree += 1
            print(
                f"  DISAGREE  {path}\n"
                f"    evaluated={evaluated:.8g}  registered={registered_float:.8g}"
                f"  rel_err={rel_err:.2%}"
            )

    total_with_desc = n_agree + n_disagree
    total = len(params)

    print("\n" + "=" * 60)
    print(f"TOTAL parameters:          {total}")
    print(f"  with eml_description:    {total_with_desc + n_no_value}")
    print(f"  AGREE  (within {tolerance:.1%}):  {n_agree}")
    print(f"  DISAGREE:                {n_disagree}")
    print(f"  SKIP (no value/error):   {n_skip + n_no_value}")
    if total_with_desc > 0:
        pct = 100 * n_agree / total_with_desc
        print(f"  Agreement rate:          {pct:.1f}%")
    print("=" * 60)

    summary = {
        "total": total,
        "with_eml_description": total_with_desc + n_no_value,
        "agree": n_agree,
        "disagree": n_disagree,
        "skip": n_skip + n_no_value,
        "agreement_rate": n_agree / total_with_desc if total_with_desc > 0 else None,
        "tolerance": tolerance,
        "results": results,
    }

    OUTPUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    def _json_safe(obj: Any) -> Any:
        if isinstance(obj, float) and not math.isfinite(obj):
            return str(obj)
        if isinstance(obj, dict):
            return {k: _json_safe(v) for k, v in obj.items()}
        if isinstance(obj, list):
            return [_json_safe(v) for v in obj]
        return obj

    with open(OUTPUT_JSON, "w", encoding="utf-8") as f:
        json.dump(_json_safe(summary), f, indent=2)
    print(f"\nDetailed results -> {OUTPUT_JSON}")

    return summary


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="Cross-check eml_description fields in parameters.json")
    parser.add_argument("--tolerance", type=float, default=DEFAULT_TOLERANCE,
                        help=f"Relative tolerance for AGREE (default {DEFAULT_TOLERANCE})")
    parser.add_argument("--verbose", action="store_true",
                        help="Print AGREE lines too (default: only DISAGREE)")
    parser.add_argument("--only-fails", action="store_true",
                        help="Only print DISAGREE/ERROR lines")
    args = parser.parse_args()

    summary = run_crosscheck(
        tolerance=args.tolerance,
        verbose=args.verbose,
        only_fails=args.only_fails,
    )

    if summary["disagree"] > 0:
        sys.exit(1)


if __name__ == "__main__":
    main()
