def invoke_claude(prompt: str, gate_id: str) -> bool:
    """
    Invoke Claude Code CLI with the review prompt.
    Returns True if Claude made changes that should be committed.
    """
    # Write prompt to temp file (avoids shell escaping issues)
    prompt_file = LOG_DIR / f"prompt_{gate_id}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
    prompt_file.parent.mkdir(parents=True, exist_ok=True)
    with open(prompt_file, 'w', encoding='utf-8') as f:
        f.write(prompt)

    print(f"  [CLAUDE] Invoking Claude Code for {gate_id}...")
    print(f"  [CLAUDE] Prompt saved to: {prompt_file.name}")

    # Invoke Claude Code CLI
    # --print mode: pipe prompt via stdin to avoid shell length limits
    # Use shell=True on Windows because 'claude' is an npm .cmd wrapper
    try:
        with open(prompt_file, 'r', encoding='utf-8') as pf:
            prompt_text = pf.read()

        import platform
        use_shell = platform.system() == "Windows"

        result = subprocess.run(
            [
                "claude",
                "--print",
                "--dangerously-skip-permissions",
                "--max-turns", "50",
            ],
            input=prompt_text,
            capture_output=True, text=True,
            cwd=str(PROJECT_ROOT),
            timeout=1800,  # 30 minute max per gate review
            shell=use_shell,
        )

        # Save response log (always, even on error)
        response_file = LOG_DIR / f"response_{gate_id}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
        output_text = result.stdout or ""
        stderr_text = result.stderr or ""
        with open(response_file, 'w', encoding='utf-8') as f:
            f.write(f"# Claude Response for {gate_id}\n")
            f.write(f"Exit code: {result.returncode}\n\n")
            f.write(output_text)
            if stderr_text:
                f.write("\n\n--- STDERR ---\n")
                f.write(stderr_text)

        print(f"  [CLAUDE] Response saved to: {response_file.name}")
        print(f"  [CLAUDE] Exit code: {result.returncode}")
        print(f"  [CLAUDE] Output length: {len(result.stdout)} chars")

        if result.returncode != 0:
            print(f"  [CLAUDE] ERROR (exit {result.returncode}): {result.stderr[:500]}")
            return False

        if len(result.stdout) < 50:
            print(f"  [CLAUDE] WARNING: Very short response — likely an error")
            return False

        # Check if Claude indicated no changes needed
        if "No changes needed" in result.stdout or "implementation is sound" in result.stdout:
            print(f"  [CLAUDE] Gate {gate_id}: No changes needed.")
            return False

        return True

    except subprocess.TimeoutExpired:
        print(f"  [CLAUDE] Timeout after 30 minutes for {gate_id}")
        return False
    except FileNotFoundError:
        print("  [CLAUDE] ERROR: 'claude' CLI not found!")
        print("  [CLAUDE] Install: npm install -g @anthropic-ai/claude-code")
        print("  [CLAUDE] On Windows, ensure Claude .cmd is in PATH")
        return False
    except Exception as e:
        print(f"  [CLAUDE] Error: {e}")
        return False


