def run_cmd(cmd: List[str], cwd=None, timeout=300) -> subprocess.CompletedProcess:
    """Run a command and return result."""
    return subprocess.run(
        cmd, capture_output=True, text=True,
        cwd=cwd or str(PROJECT_ROOT), timeout=timeout
    )

def ensure_branch():
    """Ensure we're on the GateCertReviews branch."""
    # Check current branch
    result = run_cmd(["git", "branch", "--show-current"])
    current = result.stdout.strip()

    if current == BRANCH_NAME:
        return

    # Check if branch exists
    result = run_cmd(["git", "branch", "--list", BRANCH_NAME])
    if BRANCH_NAME in result.stdout:
        run_cmd(["git", "checkout", BRANCH_NAME])
    else:
        run_cmd(["git", "checkout", "-b", BRANCH_NAME])

    print(f"  [GIT] On branch: {BRANCH_NAME}")

def has_changes() -> bool:
    result = run_cmd(["git", "status", "--porcelain"])
    # Filter out state file and log files from the diff check
    lines = [l for l in result.stdout.strip().split('\n') if l.strip()
             and not '.gate_review_state' in l
             and not 'gate_review_logs' in l]
    return len(lines) > 0

def get_diff_stat() -> str:
    result = run_cmd(["git", "diff", "--stat"])
    return result.stdout

def commit_and_push(gate_id: str, message: str) -> bool:
    """Stage, commit, and push changes."""
    run_cmd(["git", "add", "-A"])
    result = run_cmd(["git", "commit", "-m", f"{message}\n\nCo-Authored-By: Claude Opus 4.6 (1M context) <noreply@anthropic.com>"])
    if result.returncode != 0:
        print(f"  [GIT] Commit failed: {result.stderr[:200]}")
        return False

    result = run_cmd(["git", "push", "-u", "origin", BRANCH_NAME])
    if result.returncode != 0:
        print(f"  [GIT] Push failed: {result.stderr[:200]}")
        return False

    print(f"  [GIT] Pushed: {message[:60]}...")
    return True


