def build_review_queue() -> List[Dict[str, Any]]:
    """
    Build the master review queue. Each item is a dict with:
      - id: unique identifier
      - type: 'active_cert' | 'cert_json'
      - sector: sector name
      - desc: human-readable description
      - method: (active_cert only) method name in CERTIFICATES.py
      - json_path: (cert_json only) path to JSON file
    """
    queue = []

    # 1. Active certificates (from CERTIFICATES.py)
    for cert in ACTIVE_CERTIFICATES:
        queue.append({
            "id": cert["id"],
            "type": "active_cert",
            "sector": cert["sector"],
            "desc": cert["desc"],
            "method": cert["method"],
        })

    # 2. Certificate JSON files (may overlap with active certs — that's OK,
    #    the JSON review focuses on the stored data, Wolfram code, etc.)
    for cj in discover_cert_jsons():
        queue.append({
            "id": f"JSON-{cj['id']}",
            "type": "cert_json",
            "sector": cj["sector"],
            "desc": f"Certificate JSON: {cj['id']}",
            "json_path": cj["path"],
        })

    return queue


