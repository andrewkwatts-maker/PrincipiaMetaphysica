def read_file_excerpt(filepath: str, max_lines: int = 200) -> str:
    """Read up to max_lines from a file."""
    full_path = PROJECT_ROOT / filepath
    if not full_path.exists():
        return f"[File not found: {filepath}]"
    try:
        with open(full_path, 'r', encoding='utf-8', errors='replace') as f:
            lines = f.readlines()[:max_lines]
        return ''.join(lines)
    except Exception as e:
        return f"[Error reading {filepath}: {e}]"

def get_cert_method_source(method_name: str) -> str:
    """Extract the source code of a specific certificate method from CERTIFICATES.py."""
    try:
        with open(CERTIFICATES_PY, 'r', encoding='utf-8') as f:
            content = f.read()

        # Find the method
        marker = f"def {method_name}(self"
        idx = content.find(marker)
        if idx == -1:
            return f"[Method {method_name} not found in CERTIFICATES.py]"

        # Extract until next method or end of class
        end_markers = ["\n    def ", "\n    # ═"]
        end_idx = len(content)
        for em in end_markers:
            next_idx = content.find(em, idx + len(marker))
            if next_idx != -1 and next_idx < end_idx:
                end_idx = next_idx

        return content[idx:end_idx].strip()

    except Exception as e:
        return f"[Error: {e}]"

def get_cert_json_content(json_path: str) -> str:
    """Read a certificate JSON file."""
    try:
        with open(json_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        return json.dumps(data, indent=2)[:5000]  # Cap at 5k chars
    except Exception as e:
        return f"[Error reading {json_path}: {e}]"

def get_wolfram_result(proof_id: str) -> str:
    """Get Wolfram validation result for a certificate."""
    if not WOLFRAM_RESULTS.exists():
        return "[No wolfram_results.json found]"
    try:
        with open(WOLFRAM_RESULTS, 'r', encoding='utf-8') as f:
            data = json.load(f)
        certs = data.get('certificates', {})
        if proof_id in certs:
            return json.dumps(certs[proof_id], indent=2)
        # Try fuzzy match
        for key in certs:
            if proof_id.lower().replace('-', '_') in key.lower().replace('-', '_'):
                return json.dumps(certs[key], indent=2)
        return f"[No Wolfram result for {proof_id}]"
    except Exception as e:
        return f"[Error: {e}]"

