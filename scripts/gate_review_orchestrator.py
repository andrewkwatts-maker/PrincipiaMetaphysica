def discover_cert_jsons() -> List[Dict[str, str]]:
    """Discover all certificate JSON files on disk."""
    certs = []
    if CERT_JSON_DIR.exists():
        for json_file in sorted(CERT_JSON_DIR.rglob("*.json")):
            if json_file.name == "MASTER_LEDGER.json":
                continue  # Skip the aggregate ledger
            rel = json_file.relative_to(CERT_JSON_DIR)
            certs.append({
                "id": json_file.stem,
                "path": str(json_file),
                "sector": rel.parts[0] if len(rel.parts) > 1 else "general",
            })
    return certs


