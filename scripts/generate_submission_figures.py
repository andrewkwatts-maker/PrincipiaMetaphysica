def generate_figure3_alp_discovery_window():
    """
    Figure 3: ALP Discovery Window (IAXO Sensitivity)
    X-axis: ALP mass m_a (meV) - log scale
    Y-axis: Photon coupling g_aγγ (GeV⁻¹) - log scale
    PM Prediction: Red star at (3.51 meV, 10⁻¹¹ GeV⁻¹)
    """
    print("\n[Figure 3] Generating ALP Discovery Window...")

    fig, ax = plt.subplots(figsize=(12, 10))

    # Mass range (meV)
    m_a = np.logspace(-1, 1, 200)  # 0.1 to 10 meV

    # CAST exclusion limit (approximate)
    # CAST: g_aγγ < 0.66 × 10⁻¹⁰ GeV⁻¹ for m_a < 0.02 eV = 20 meV
    cast_limit = np.full_like(m_a, 0.66e-10)
    cast_limit[m_a > 20] = np.nan  # CAST only excludes up to ~20 meV

    # IAXO projected sensitivity (2025-2028)
    # IAXO: g_aγγ ~ 10⁻¹¹ GeV⁻¹ for m_a ~ 0.01-10 meV
    iaxo_sensitivity = np.full_like(m_a, 1.5e-11)
    iaxo_sensitivity[m_a < 0.5] = 1.5e-11 * (0.5 / m_a[m_a < 0.5])**0.3  # Slightly worse at low mass
    iaxo_sensitivity[m_a > 10] = np.nan

    # BabyIAXO sensitivity (intermediate step)
    babyiaxo_sensitivity = np.full_like(m_a, 1.5e-10)
    babyiaxo_sensitivity[m_a > 15] = np.nan

    # PM Prediction: m_a = 3.51 meV, g_aγγ = 10⁻¹¹ GeV⁻¹
    pm_mass = 3.51  # meV
    pm_coupling = 1.0e-11  # GeV⁻¹
    pm_mass_error = 0.5  # meV (uncertainty)
    pm_coupling_error = 0.3e-11  # GeV⁻¹ (uncertainty)

    # Plot exclusion regions
    ax.fill_between(m_a, cast_limit, 1e-8, where=~np.isnan(cast_limit),
                    alpha=0.4, color='red', label='CAST Excluded (2017)')
    ax.plot(m_a, cast_limit, 'r-', linewidth=2)

    # Plot future sensitivities
    ax.plot(m_a, babyiaxo_sensitivity, 'b--', linewidth=2.5, label='BabyIAXO Projected (2024-2025)')
    ax.plot(m_a, iaxo_sensitivity, 'purple', linewidth=3, label='IAXO Projected (2025-2028)')
    ax.fill_between(m_a, iaxo_sensitivity, 1e-13, where=~np.isnan(iaxo_sensitivity),
                    alpha=0.2, color='purple')

    # PM Prediction with error box
    ax.errorbar(pm_mass, pm_coupling,
               xerr=pm_mass_error, yerr=pm_coupling_error,
               fmt='*', color='red', markersize=25,
               markeredgecolor='darkred', markeredgewidth=2,
               ecolor='darkred', elinewidth=3, capsize=8, capthick=3,
               label='PM Prediction', zorder=10)

    # Discovery window annotation
    ax.annotate('Detection Window\n2025-2028',
               xy=(pm_mass, pm_coupling), xytext=(1.2, 5e-11),
               fontsize=13, fontweight='bold', color='purple',
               bbox=dict(boxstyle='round,pad=0.5', facecolor='yellow', alpha=0.7, edgecolor='purple', linewidth=2),
               arrowprops=dict(arrowstyle='->', lw=3, color='purple', connectionstyle='arc3,rad=0.3'))

    # Principia Metric label
    ax.text(pm_mass, pm_coupling * 0.3, 'Principia Metric\nPrediction',
           ha='center', fontsize=11, fontweight='bold', color='darkred',
           bbox=dict(boxstyle='round,pad=0.4', facecolor='white', alpha=0.8, edgecolor='red', linewidth=1.5))

    # Axes and labels
    ax.set_xscale('log')
    ax.set_yscale('log')
    ax.set_xlabel(r'ALP Mass $m_a$ (meV)', fontsize=16, fontweight='bold')
    ax.set_ylabel(r'Photon Coupling $g_{a\gamma\gamma}$ (GeV$^{-1}$)', fontsize=16, fontweight='bold')
    ax.set_title('ALP Discovery Window: IAXO Sensitivity vs. PM Prediction\n'
                r'Axion-Like Particle Detection Prospects (2025-2028)',
                fontsize=18, fontweight='bold', pad=20)

    ax.set_xlim(0.1, 10)
    ax.set_ylim(1e-12, 1e-8)

    # Legend
    ax.legend(loc='upper right', fontsize=12, framealpha=0.9, edgecolor='black', fancybox=True)

    # Grid
    ax.grid(True, which='both', alpha=0.3, linestyle='--', linewidth=0.8)

    # Save figure
    output_path = PLOTS_DIR / "figure3_alp_discovery_window.png"
    plt.savefig(output_path, dpi=300, bbox_inches='tight', facecolor='white')
    output_path_pdf = PLOTS_DIR / "figure3_alp_discovery_window.pdf"
    plt.savefig(output_path_pdf, bbox_inches='tight', facecolor='white')
    plt.close()

    print(f"  OK Saved: {output_path}")
    print(f"  OK Saved: {output_path_pdf}")


