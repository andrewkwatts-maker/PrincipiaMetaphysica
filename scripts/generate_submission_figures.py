# FIGURE 2: GLOBAL CONVERGENCE SCATTER PLOT
# =============================================================================

def generate_figure2_convergence_scatter(params_data):
    """
    Figure 2: Global Convergence Scatter Plot
    X-axis: PM Predicted Values
    Y-axis: Experimental Values
    Color: PASS (green), MARGINAL (yellow), FAIL (red)
    """
    print("\n[Figure 2] Generating Global Convergence Scatter Plot...")

    # Extract testable parameters (with experimental values)
    params = params_data['parameters']

    testable = []
    for param_id, data in params.items():
        if (data.get('experimental_value') is not None and
            data.get('value') is not None and
            data.get('validation_status') in ['PASS', 'MARGINAL', 'FAIL']):

            # Skip if values are exactly identical (likely inputs)
            if data['value'] != data['experimental_value']:
                sigma_dev = data.get('sigma_deviation', 0)
                if sigma_dev is None:
                    sigma_dev = 0

                uncertainty = data.get('experimental_uncertainty', 0)
                if uncertainty is None or uncertainty == 0:
                    uncertainty = abs(data['value'] - data['experimental_value']) * 0.01  # 1% default

                testable.append({
                    'id': param_id,
                    'predicted': data['value'],
                    'experimental': data['experimental_value'],
                    'uncertainty': uncertainty,
                    'status': data.get('validation_status', 'UNKNOWN'),
                    'sigma': abs(sigma_dev)
                })

    print(f"  Found {len(testable)} testable parameters")

    if len(testable) == 0:
        print("  WARNING: No testable parameters found!")
        return

    # Prepare data arrays
    predicted = np.array([p['predicted'] for p in testable])
    experimental = np.array([p['experimental'] for p in testable])
    uncertainties = np.array([p['uncertainty'] for p in testable])
    statuses = np.array([p['status'] for p in testable])
    sigmas = np.array([p['sigma'] for p in testable])

    # Create figure
    fig, ax = plt.subplots(figsize=(12, 12))

    # Color mapping
    color_map = {'PASS': 'green', 'MARGINAL': 'orange', 'FAIL': 'red'}
    colors = [color_map.get(s, 'gray') for s in statuses]

    # Determine if log scale is needed
    use_log = (np.max(np.abs(predicted)) / np.min(np.abs(predicted[predicted != 0])) > 1000)

    if use_log:
        # Log scale scatter
        ax.set_xscale('log')
        ax.set_yscale('log')

        # Filter out zeros for log scale
        mask = (predicted > 0) & (experimental > 0)
        predicted_log = predicted[mask]
        experimental_log = experimental[mask]
        uncertainties_log = uncertainties[mask]
        colors_log = [colors[i] for i in range(len(colors)) if mask[i]]

        # Scatter plot
        ax.scatter(predicted_log, experimental_log, c=colors_log,
                  s=100, alpha=0.7, edgecolors='black', linewidth=1.5, zorder=3)

        # Error bars
        ax.errorbar(predicted_log, experimental_log, yerr=uncertainties_log,
                   fmt='none', ecolor='gray', alpha=0.4, capsize=3, zorder=2)

        # Diagonal line (perfect agreement)
        min_val = min(np.min(predicted_log), np.min(experimental_log))
        max_val = max(np.max(predicted_log), np.max(experimental_log))
        ax.plot([min_val, max_val], [min_val, max_val],
               'k--', linewidth=2, label='Perfect Agreement (y = x)', zorder=1)

    else:
        # Linear scale scatter
        ax.scatter(predicted, experimental, c=colors,
                  s=100, alpha=0.7, edgecolors='black', linewidth=1.5, zorder=3)

        # Error bars
        ax.errorbar(predicted, experimental, yerr=uncertainties,
                   fmt='none', ecolor='gray', alpha=0.4, capsize=3, zorder=2)

        # Diagonal line
        min_val = min(np.min(predicted), np.min(experimental))
        max_val = max(np.max(predicted), np.max(experimental))
        ax.plot([min_val, max_val], [min_val, max_val],
               'k--', linewidth=2, label='Perfect Agreement (y = x)', zorder=1)

    # Annotate key parameters (highest sigma deviations)
    key_params = sorted(testable, key=lambda p: p['sigma'], reverse=True)[:5]
    for param in key_params:
        if use_log and (param['predicted'] <= 0 or param['experimental'] <= 0):
            continue

        label = param['id'].split('.')[-1]  # Extract short name
        ax.annotate(label,
                   xy=(param['predicted'], param['experimental']),
                   xytext=(10, 10), textcoords='offset points',
                   fontsize=9, alpha=0.8,
                   bbox=dict(boxstyle='round,pad=0.3', facecolor='yellow', alpha=0.5),
                   arrowprops=dict(arrowstyle='->', connectionstyle='arc3,rad=0.2',
                                  color='black', lw=1))

    # Labels and title
    ax.set_xlabel('PM Predicted Values', fontsize=16, fontweight='bold')
    ax.set_ylabel('Experimental Values', fontsize=16, fontweight='bold')
    ax.set_title(f'Global Convergence: PM Predictions vs. Experiment\n({len(testable)} Testable Parameters)',
                fontsize=18, fontweight='bold', pad=20)

    # Legend
    legend_elements = [
        Line2D([0], [0], marker='o', color='w', label='PASS (≤2σ)',
              markerfacecolor='green', markersize=10, markeredgecolor='black'),
        Line2D([0], [0], marker='o', color='w', label='MARGINAL (2-3σ)',
              markerfacecolor='orange', markersize=10, markeredgecolor='black'),
        Line2D([0], [0], marker='o', color='w', label='FAIL (>3σ)',
              markerfacecolor='red', markersize=10, markeredgecolor='black'),
        Line2D([0], [0], color='black', linestyle='--', linewidth=2, label='Perfect Agreement')
    ]
    ax.legend(handles=legend_elements, loc='upper left', fontsize=12, framealpha=0.9)

    # Grid
    ax.grid(True, alpha=0.3, linestyle='--', linewidth=0.8)

    # Statistics text box
    pass_count = np.sum(statuses == 'PASS')
    marginal_count = np.sum(statuses == 'MARGINAL')
    fail_count = np.sum(statuses == 'FAIL')

    stats_text = (f"Statistics:\n"
                 f"PASS: {pass_count} ({100*pass_count/len(testable):.1f}%)\n"
                 f"MARGINAL: {marginal_count} ({100*marginal_count/len(testable):.1f}%)\n"
                 f"FAIL: {fail_count} ({100*fail_count/len(testable):.1f}%)")

    ax.text(0.98, 0.02, stats_text,
           transform=ax.transAxes, fontsize=11,
           verticalalignment='bottom', horizontalalignment='right',
           bbox=dict(boxstyle='round', facecolor='white', alpha=0.8, edgecolor='black'))

    # Save figure
    output_path = PLOTS_DIR / "figure2_convergence_scatter.png"
    plt.savefig(output_path, dpi=300, bbox_inches='tight', facecolor='white')
    output_path_pdf = PLOTS_DIR / "figure2_convergence_scatter.pdf"
    plt.savefig(output_path_pdf, bbox_inches='tight', facecolor='white')
    plt.close()

    print(f"  OK Saved: {output_path}")
    print(f"  OK Saved: {output_path_pdf}")


