import numpy as np


# The 7 lines of the Fano plane (same set of lines as G2_TRIPLES).
# Used for the G2 3-form φ (all positive orientation).
FANO_TRIPLES = [
    (0, 1, 2),
    (0, 3, 4),
    (0, 5, 6),
    (1, 3, 5),
    (1, 4, 6),
    (2, 3, 6),
    (2, 4, 5),
]

# Octonion multiplication table with correct SIGNS for a normed
# division algebra (|ab| = |a||b|). Same Fano plane lines but
# with orientation derived from the standard cyclic labeling:
#   (0,1,3), (1,2,4), (2,3,5), (3,4,6), (4,5,0), (5,6,1), (6,0,2)
# mapped to our labeling via σ: 0→0, 1→1, 2→3, 3→4, 4→5, 5→2, 6→6.
# The sign on triple (1,3,5) must be -1 for norm multiplicativity.
OCTONION_TRIPLES = [
    (0, 1, 2, +1),
    (0, 3, 4, +1),
    (0, 5, 6, +1),
    (1, 3, 5, -1),   # flipped for norm multiplicativity
    (1, 4, 6, +1),
    (2, 3, 6, +1),
    (2, 4, 5, +1),
]


class OctonionAlgebra:
    """The octonion algebra O with Fano plane multiplication.

    Octonions are 8-dimensional: one real unit 1 and seven imaginary
    units e₀, ..., e₆. Multiplication is neither commutative nor
    associative, but is alternative (every subalgebra generated by
    two elements is associative).

    The automorphism group Aut(O) = G₂, acting on Im(O) = R⁷.
    """

    def __init__(self):
        self._C_geom = self._build_geometric_constants()
        self._C_mult = self._build_multiplication_constants()

    @staticmethod
    def _build_geometric_constants() -> np.ndarray:
        """Build the (7,7,7) G2 3-form tensor φ_{ijk} (all-positive orientation).

        This is the G2 associative 3-form, matching G2_TRIPLES in
        g2_differential.py. Used for geometry (metric, Hodge star, etc).
        """
        C = np.zeros((7, 7, 7), dtype=np.float64)
        for (i, j, k) in FANO_TRIPLES:
            C[i, j, k] = +1.0
            C[j, k, i] = +1.0
            C[k, i, j] = +1.0
            C[i, k, j] = -1.0
            C[k, j, i] = -1.0
            C[j, i, k] = -1.0
        return C

    @staticmethod
    def _build_multiplication_constants() -> np.ndarray:
        """Build the (7,7,7) octonion multiplication tensor.

        Uses OCTONION_TRIPLES with correct signs for a normed
        division algebra (|ab| = |a||b|). The sign on triple (1,3,5)
        is -1 compared to the all-positive G2 form.

        The imaginary octonion multiplication rule is:
          e_i · e_j = -δ_{ij} + C_{ijk} e_k
        """
        C = np.zeros((7, 7, 7), dtype=np.float64)
        for (i, j, k, s) in OCTONION_TRIPLES:
            C[i, j, k] = s
            C[j, k, i] = s
            C[k, i, j] = s
            C[i, k, j] = -s
            C[k, j, i] = -s
            C[j, i, k] = -s
        return C

    @property
    def structure_constants(self) -> np.ndarray:
        """The (7,7,7) G2 3-form tensor φ_{ijk} (geometric structure constants).

        These are the all-positive-orientation Fano plane structure
        constants, matching G2_TRIPLES in g2_differential.py.
        Used for G2 geometry (metric derivation, Hodge star, etc).
        """
        return self._C_geom.copy()

    @property
    def multiplication_constants(self) -> np.ndarray:
        """The (7,7,7) octonion multiplication tensor (sign-corrected).

        These include the correct signs for norm multiplicativity
        (|ab| = |a||b|), differing from the geometric structure
        constants by the sign on triple (1,3,5).
        """
        return self._C_mult.copy()

    def g2_structure_as_3form(self) -> np.ndarray:
        """Return the G2 associative 3-form φ derived from octonion structure.

        The G2 3-form uses the all-positive Fano plane orientation,
        which is the correct convention for metric derivation via
        Hitchin's formula (g_{ij} = φ_{iab}φ_{jab}/6 = δ_{ij}).

        G₂ = Aut(O) preserves both the multiplication and the 3-form,
        connecting octonion algebra to G2 differential geometry.

        Returns:
            (7,7,7) antisymmetric tensor — identical to the standard
            G2 3-form from g2_differential.py
        """
        return self._C_geom.copy()

    def multiply(self, a: np.ndarray, b: np.ndarray) -> np.ndarray:
        """Multiply two octonions.

        An octonion is represented as an 8-vector:
          x = x₀·1 + x₁·e₀ + x₂·e₁ + ... + x₇·e₆

        where x[0] is the real part and x[1:8] is the imaginary part.

        Multiplication rule:
          1·eᵢ = eᵢ·1 = eᵢ
          eᵢ·eⱼ = -δᵢⱼ + Cᵢⱼₖ·eₖ

        Args:
            a: 8-vector (first octonion)
            b: 8-vector (second octonion)

        Returns:
            8-vector (product octonion)
        """
        result = np.zeros(8, dtype=np.float64)
        a0, a_im = a[0], a[1:]
        b0, b_im = b[0], b[1:]

        # Real part: a₀b₀ - Σᵢ aᵢbᵢ
        result[0] = a0 * b0 - np.dot(a_im, b_im)

        # Imaginary part: a₀bᵢ + aᵢb₀ + Cᵢⱼₖ aⱼbₖ
        # Uses multiplication constants (sign-corrected for norm multiplicativity)
        result[1:] = a0 * b_im + b0 * a_im + np.einsum('ijk,j,k->i', self._C_mult, a_im, b_im)

        return result

    def norm_squared(self, x: np.ndarray) -> float:
        """Compute the octonion norm squared: |x|² = x·x̄ = Σxᵢ²."""
        return float(np.dot(x, x))

    def conjugate(self, x: np.ndarray) -> np.ndarray:
        """Octonion conjugate: x̄ = x₀ - Σxᵢeᵢ."""
        result = x.copy()
        result[1:] = -result[1:]
        return result

    @staticmethod
    def project_to_imaginary(v: np.ndarray) -> np.ndarray:
        """Project from R⁸ (full octonion space) to Im(O) = R⁷.

        Drops the real (first) coordinate.

        Args:
            v: 8-vector or (N, 8) array

        Returns:
            7-vector or (N, 7) array
        """
        if v.ndim == 1:
            return v[1:].copy()
        return v[:, 1:].copy()

    @staticmethod
    def embed_imaginary(v: np.ndarray) -> np.ndarray:
        """Embed from Im(O) = R⁷ to R⁸ (with zero real part).

        Args:
            v: 7-vector or (N, 7) array

        Returns:
            8-vector or (N, 8) array with v[0] = 0
        """
        if v.ndim == 1:
            return np.concatenate([[0.0], v])
        return np.column_stack([np.zeros(len(v)), v])

    def verify(self) -> dict:
        """Verify octonion algebra properties."""
        checks = {}

        # 1. Structure constants are totally antisymmetric
        C = self._C_mult
        checks['antisymmetric'] = bool(
            np.allclose(C, -np.transpose(C, (1, 0, 2)))
            and np.allclose(C, -np.transpose(C, (0, 2, 1)))
        )

        # Geometric constants also antisymmetric
        Cg = self._C_geom
        checks['geom_antisymmetric'] = bool(
            np.allclose(Cg, -np.transpose(Cg, (1, 0, 2)))
            and np.allclose(Cg, -np.transpose(Cg, (0, 2, 1)))
        )

        # 2. Norm is multiplicative: |ab|² = |a|²|b|²
        rng = np.random.RandomState(42)
        a = rng.randn(8)
        b = rng.randn(8)
        ab = self.multiply(a, b)
        checks['norm_multiplicative'] = bool(np.isclose(
            self.norm_squared(ab),
            self.norm_squared(a) * self.norm_squared(b),
            rtol=1e-10
        ))

        # 3. Alternative property: a(ab) = a²b and (ab)b = ab²
        a2 = self.multiply(a, a)
        checks['left_alternative'] = bool(np.allclose(
            self.multiply(a, self.multiply(a, b)),
            self.multiply(a2, b),
            atol=1e-10
        ))
        b2 = self.multiply(b, b)
        checks['right_alternative'] = bool(np.allclose(
            self.multiply(self.multiply(a, b), b),
            self.multiply(a, b2),
            atol=1e-10
        ))

        # 4. Not associative in general
        c = rng.randn(8)
        lhs = self.multiply(self.multiply(a, b), c)
        rhs = self.multiply(a, self.multiply(b, c))
        checks['not_associative'] = bool(not np.allclose(lhs, rhs, atol=1e-10))

        # 5. Conjugate satisfies x·x̄ = |x|²
        x = rng.randn(8)
        xx_bar = self.multiply(x, self.conjugate(x))
        checks['conjugate_norm'] = bool(
            np.isclose(xx_bar[0], self.norm_squared(x), rtol=1e-10)
            and np.allclose(xx_bar[1:], 0, atol=1e-10)
        )

        # 6. Seven Fano triples present
        checks['seven_triples'] = len(FANO_TRIPLES) == 7

        return checks
