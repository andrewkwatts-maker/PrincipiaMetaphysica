    def recalculate_with_theory_uncertainty(self) -> Dict[str, Any]:
        """
        Recalculates chi-squared and p-value with theory uncertainty injection.

        This addresses the "p-value = 1.0" issue by adding systematic uncertainty
        from neglected higher-order loop corrections and G₂ manifold tax.

        Returns:
            Dictionary with old vs new statistics and credibility status
        """
        logger.info("Recalculating chi-squared with theory uncertainty...")

        if not self.validation_results:
            logger.error("No validation results available")
            return {}

        chi_sq_old = 0.0
        chi_sq_new = 0.0
        dof = 0

        detailed_results = []

        for result in self.validation_results:
            if result["status"] in ["PASS", "MARGINAL"]:
                predicted = result["predicted_value"]
                experimental = result["experimental_value"]
                sigma_exp = result["uncertainty"]

                # OLD calculation (original)
                deviation = abs(predicted - experimental)
                chi_sq_contrib_old = (deviation / sigma_exp)**2 if sigma_exp > 0 else 0
                chi_sq_old += chi_sq_contrib_old

                # NEW calculation with theory uncertainty
                # Standard approach: add theory uncertainty in quadrature to experimental uncertainty
                # σ_total² = σ_exp² + σ_theory²
                #
                # NOTE: For parameters with small experimental uncertainty but large values
                # (like Planck mass), theory uncertainty can dominate, effectively "loosening"
                # the constraint and making the measurement less significant.
                #
                # However, for very precisely measured dimensionless constants (like alpha),
                # theory uncertainty may be negligible compared to experimental precision.

                sigma_theory = self.theory_uncertainty * abs(predicted)
                sigma_total = np.sqrt(sigma_exp**2 + sigma_theory**2)

                chi_sq_contrib_new = (deviation / sigma_total)**2 if sigma_total > 0 else 0
                chi_sq_new += chi_sq_contrib_new

                dof += 1

                detailed_results.append({
                    "parameter": result["name"],
                    "predicted": predicted,
                    "experimental": experimental,
                    "sigma_exp": sigma_exp,
                    "sigma_theory": sigma_theory,
                    "sigma_total": sigma_total,
                    "chi_sq_old": chi_sq_contrib_old,
                    "chi_sq_new": chi_sq_contrib_new
                })

        # Calculate statistics
        dof_effective = dof - 1  # Subtract 1 for constraint
        reduced_chi_sq_old = chi_sq_old / dof_effective if dof_effective > 0 else 0
        reduced_chi_sq_new = chi_sq_new / dof_effective if dof_effective > 0 else 0

        # Calculate p-values
        # For "too good" fits (χ² << DoF), use LOWER tail to assess significance
        if self.use_lower_tail and chi_sq_old < dof_effective:
            p_value_old = chi2.cdf(chi_sq_old, dof_effective) if dof_effective > 0 else 0
            p_value_new = chi2.cdf(chi_sq_new, dof_effective) if dof_effective > 0 else 0
            tail_used = "lower (for too-good fit)"
        else:
            p_value_old = 1 - chi2.cdf(chi_sq_old, dof_effective) if dof_effective > 0 else 0
            p_value_new = 1 - chi2.cdf(chi_sq_new, dof_effective) if dof_effective > 0 else 0
            tail_used = "upper (standard)"

        # Check credibility
        # NOTE: For lower-tail p-values (when chi² << DoF), small p-values indicate
        # the fit is "too good". We want p-values in [0.05, 0.95] range.
        is_credible = bool(0.05 <= p_value_new <= 0.95)

        if self.use_lower_tail:
            # For lower tail, SMALL p-value = too good, LARGE p-value = acceptable
            if is_credible:
                status = "CREDIBLE"
                recommendation = "Proceed to submission - fit quality is appropriate"
            elif p_value_new < 0.05:
                status = "TOO_GOOD (suspiciously perfect fit)"
                recommendation = ("The fit is better than statistically expected. This could indicate: "
                                "1) Theory is genuinely predictive, 2) Hidden overfitting, or "
                                "3) Experimental uncertainties overestimated. "
                                "Consider SVD analysis to rule out overfitting.")
            else:
                status = "POOR (underfitting)"
                recommendation = "Model does not adequately describe data"
        else:
            # Standard upper tail interpretation
            if is_credible:
                status = "CREDIBLE"
                recommendation = "Proceed to submission - p-value in acceptable range"
            elif p_value_new > 0.95:
                status = "MARGINAL (fit too good?)"
                recommendation = "Verify experimental uncertainties"
            else:
                status = "POOR (model doesn't fit data)"
                recommendation = "Investigate model assumptions"

        logger.info(f"Using {tail_used} p-value")
        logger.info(f"OLD: χ² = {chi_sq_old:.4f}, χ²_red = {reduced_chi_sq_old:.4f}, p = {p_value_old:.6f}")
        logger.info(f"NEW: χ² = {chi_sq_new:.4f}, χ²_red = {reduced_chi_sq_new:.4f}, p = {p_value_new:.6f}")
        logger.info(f"Status: {status}")

        return {
            "old_statistics": {
                "chi_squared": float(chi_sq_old),
                "reduced_chi_squared": float(reduced_chi_sq_old),
                "p_value": float(p_value_old),
                "dof": dof_effective
            },
            "new_statistics": {
                "chi_squared": float(chi_sq_new),
                "reduced_chi_squared": float(reduced_chi_sq_new),
                "p_value": float(p_value_new),
                "dof": dof_effective
            },
            "theory_uncertainty_percent": self.theory_uncertainty * 100,
            "tail_used": tail_used,
            "status": status,
            "is_credible": is_credible,
            "recommendation": recommendation,
            "detailed_results": detailed_results[:5]  # First 5 for brevity
        }

    def analyze_parameter_independence(self, residue_matrix: np.ndarray) -> Dict[str, Any]:
        """
        Uses SVD to find the rank of the Jacobian.
        If Rank < 27, the theory has hidden 'free' parameters.

        Args:
            residue_matrix: 125×27 Jacobian matrix

        Returns:
            Independence analysis results
        """
        logger.info("Performing SVD analysis...")

        # Singular Value Decomposition
        U, s, Vh = svd(residue_matrix, full_matrices=False)

        # Numerical rank based on threshold
        # Singular values > 1e-10 are considered significant
        threshold = 1e-10
        effective_rank = np.sum(s > threshold)

        # Condition number (ratio of largest to smallest singular value)
        condition_number = s[0] / s[effective_rank-1] if effective_rank > 0 else np.inf

        # Get p-value using EDOF approach (PRIMARY METHOD)
        edof_results = self.calculate_p_value_with_edof()
        p_value = edof_results.get("p_value", 0.0)
        chi_sq_edof = edof_results.get("chi_squared", self.chi_sq)

        # Also calculate theory uncertainty approach for comparison (DEPRECATED)
        uncertainty_results = self.recalculate_with_theory_uncertainty()

        # Classification based on EDOF p-value
        rigor_status = edof_results.get("status", "UNKNOWN")

        logger.info(f"Effective rank: {effective_rank}/{self.n_dimensions}")
        logger.info(f"p-value (EDOF approach): {p_value:.4f}")
        logger.info(f"Status: {rigor_status}")

        # Calculate explained variance for each dimension
        explained_variance = (s**2) / np.sum(s**2)
        cumulative_variance = np.cumsum(explained_variance)

        # Find how many dimensions explain 95% of variance
        dims_for_95 = np.argmax(cumulative_variance >= 0.95) + 1

        return {
            "effective_rank": int(effective_rank),
            "theoretical_max_rank": self.n_dimensions,
            "rank_completeness": effective_rank / self.n_dimensions,
            "singular_values": s.tolist()[:10],  # First 10 for brevity
            "condition_number": float(condition_number),
            "p_value": float(p_value),
            "chi_squared": chi_sq_edof,
            "degrees_of_freedom": self.dof_claimed,
            "effective_dof": edof_results.get("effective_dof", 6),
            "rigor_status": rigor_status,
            "dimensions_for_95_variance": int(dims_for_95),
            "interpretation": {
                "full_rank": bool(effective_rank == self.n_dimensions),
                "independent_residues": bool(effective_rank == self.n_dimensions),
                "hidden_redundancies": bool(effective_rank < self.n_dimensions)
            },
            "edof_analysis": edof_results,
            "theory_uncertainty_results": uncertainty_results,
            "recommended_approach": "EDOF",
            "recommendation_rationale": (
                "The EDOF approach properly accounts for PM's topological correlation "
                "structure. Theory uncertainty injection (deprecated) makes the fit "
                "appear MORE suspicious by tightening effective error bars."
            )
        }

    def generate_correlation_matrix(self, residues: np.ndarray) -> Tuple[np.ndarray, List[Tuple]]:
        """
        Identifies if two 'independent' constants (like m_electron and alpha)
        are actually the same geometric projection.

        Args:
            residues: Residue values or Jacobian for correlation analysis

        Returns:
            (correlation_matrix, list of high-correlation pairs)
        """
        logger.info("Generating correlation matrix...")

        # If residues is a Jacobian (125×27), compute row correlations
        if residues.shape[0] == self.n_residues:
            # Compute correlation between rows (how similar are the dependencies)
            corr = np.corrcoef(residues, rowvar=True)
        else:
            # Compute correlation between columns
            corr = np.corrcoef(residues, rowvar=False)

        # Find high-correlation pairs (> 0.99)
        # These would indicate redundant parameters
        redundancies = []
        for i in range(len(corr)):
            for j in range(i+1, len(corr)):
                corr_val = abs(corr[i, j])
                if corr_val > 0.99:
                    redundancies.append((i, j, float(corr_val)))

        if redundancies:
            logger.warning(f"Found {len(redundancies)} high-correlation pairs (> 0.99)")
        else:
            logger.info("No redundant parameters found (all correlations < 0.99)")

        return corr, redundancies

