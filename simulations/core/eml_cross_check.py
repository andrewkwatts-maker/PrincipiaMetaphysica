"""
EML Math Cross-Check System
============================

Verifies that every simulation's run_eml() path produces values that
agree with its run() path to within a configurable tolerance.

Generates AutoGenerated/eml_cross_check.json with a full agreement report.

Copyright (c) 2025-2026 Andrew Keith Watts. All rights reserved.
"""

from __future__ import annotations

import json
import logging
import math
from dataclasses import dataclass, field, asdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional

from simulations.core.eml_integration import eml_agree, eml_deviation, EML_AVAILABLE

logger = logging.getLogger("principia.eml_cross_check")

# Default tolerance for agreement
DEFAULT_RTOL = 1e-6


@dataclass
class ParamCheck:
    """Agreement check result for a single parameter."""
    path: str
    normal_value: Any
    eml_value: Any
    deviation: float
    status: str  # "AGREE" | "FAIL" | "SKIP" | "TYPE_MISMATCH"
    note: str = ""


@dataclass
class SimulationCheck:
    """Agreement check result for one simulation."""
    simulation_id: str
    status: str  # "AGREE" | "FAIL" | "NOT_IMPLEMENTED" | "ERROR" | "SKIP"
    params_checked: int = 0
    params_agreed: int = 0
    max_deviation: float = 0.0
    param_checks: List[ParamCheck] = field(default_factory=list)
    error: Optional[str] = None
    note: str = ""


def check_simulation(
    simulation,
    registry,
    normal_result: Dict[str, Any],
    rtol: float = DEFAULT_RTOL,
) -> SimulationCheck:
    """
    Run run_eml() for one simulation and compare results against normal_result.

    Args:
        simulation: SimulationBase instance
        registry: PMRegistry instance
        normal_result: Dict returned by simulation.run(registry)
        rtol: Relative tolerance for agreement

    Returns:
        SimulationCheck with full per-parameter results
    """
    sim_id = simulation.metadata.id

    # Check if EML is available at all
    if not EML_AVAILABLE:
        return SimulationCheck(
            simulation_id=sim_id,
            status="SKIP",
            note="eml_math library not installed",
        )

    # Try to call run_eml
    try:
        eml_result = simulation.run_eml(registry)
    except NotImplementedError:
        return SimulationCheck(
            simulation_id=sim_id,
            status="NOT_IMPLEMENTED",
            note=f"{simulation.__class__.__name__}.run_eml() not yet implemented",
        )
    except Exception as exc:
        logger.warning("run_eml() raised for %s: %s", sim_id, exc)
        return SimulationCheck(
            simulation_id=sim_id,
            status="ERROR",
            error=str(exc),
        )

    # Compare numeric outputs (skip metadata keys starting with '_')
    param_checks: List[ParamCheck] = []
    max_deviation = 0.0
    all_agreed = True

    for path, normal_val in normal_result.items():
        if path.startswith("_"):
            continue  # skip internal metadata keys

        eml_val = eml_result.get(path)

        if eml_val is None:
            param_checks.append(ParamCheck(
                path=path,
                normal_value=normal_val,
                eml_value=None,
                deviation=float("nan"),
                status="SKIP",
                note="key missing from run_eml() result",
            ))
            continue

        # Numeric comparison
        if isinstance(normal_val, (int, float)) and isinstance(eml_val, (int, float)):
            nv = float(normal_val)
            ev = float(eml_val)
            if math.isnan(nv) or math.isnan(ev):
                status = "SKIP"
                dev = float("nan")
                note = "NaN value"
            elif math.isinf(nv) or math.isinf(ev):
                status = "SKIP"
                dev = float("nan")
                note = "inf value"
            else:
                dev = eml_deviation(nv, ev)
                max_deviation = max(max_deviation, dev)
                agreed = eml_agree(nv, ev, rtol=rtol)
                status = "AGREE" if agreed else "FAIL"
                if not agreed:
                    all_agreed = False
                note = ""
            param_checks.append(ParamCheck(
                path=path,
                normal_value=nv,
                eml_value=ev,
                deviation=dev,
                status=status,
                note=note,
            ))
        else:
            # Non-numeric: equality check
            agreed = (normal_val == eml_val)
            if not agreed:
                all_agreed = False
            param_checks.append(ParamCheck(
                path=path,
                normal_value=normal_val,
                eml_value=eml_val,
                deviation=0.0 if agreed else float("nan"),
                status="AGREE" if agreed else "TYPE_MISMATCH",
            ))

    numeric_checks = [c for c in param_checks if c.status not in ("SKIP",)]
    agreed_checks = [c for c in numeric_checks if c.status == "AGREE"]

    overall = "AGREE" if all_agreed else "FAIL"

    return SimulationCheck(
        simulation_id=sim_id,
        status=overall,
        params_checked=len(numeric_checks),
        params_agreed=len(agreed_checks),
        max_deviation=max_deviation,
        param_checks=param_checks,
    )


def generate_cross_check_report(
    checks: List[SimulationCheck],
    rtol: float = DEFAULT_RTOL,
    output_path: Optional[Path] = None,
) -> Dict[str, Any]:
    """
    Build the eml_cross_check.json content from a list of SimulationChecks.

    Args:
        checks: List of SimulationCheck results
        rtol: Tolerance used for this run
        output_path: If provided, write JSON to this path

    Returns:
        The report dict
    """
    total = len(checks)
    agreed = sum(1 for c in checks if c.status == "AGREE")
    failed = sum(1 for c in checks if c.status == "FAIL")
    not_impl = sum(1 for c in checks if c.status == "NOT_IMPLEMENTED")
    errors = sum(1 for c in checks if c.status == "ERROR")
    skipped = sum(1 for c in checks if c.status in ("SKIP",))

    all_deviations = [c.max_deviation for c in checks if not math.isnan(c.max_deviation)]
    global_max_dev = max(all_deviations) if all_deviations else 0.0

    # Build simulation-level summary (omit per-param detail to keep JSON lean)
    sim_summary: Dict[str, Any] = {}
    for c in checks:
        sim_summary[c.simulation_id] = {
            "status": c.status,
            "params_checked": c.params_checked,
            "params_agreed": c.params_agreed,
            "max_deviation": c.max_deviation if not math.isnan(c.max_deviation) else None,
            "note": c.note or c.error or "",
        }

    # Build failed params detail (only failures, for easy debugging)
    failed_params: Dict[str, Any] = {}
    for c in checks:
        for pc in c.param_checks:
            if pc.status == "FAIL":
                failed_params[pc.path] = {
                    "simulation": c.simulation_id,
                    "normal_value": pc.normal_value,
                    "eml_value": pc.eml_value,
                    "deviation": pc.deviation,
                }

    report = {
        "version": "24.2",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "tolerance": rtol,
        "eml_available": EML_AVAILABLE,
        "summary": {
            "total_simulations": total,
            "agreed": agreed,
            "failed": failed,
            "not_implemented": not_impl,
            "errors": errors,
            "skipped": skipped,
            "global_max_deviation": global_max_dev,
            "all_agree": (failed == 0 and errors == 0),
        },
        "simulations": sim_summary,
        "failed_params": failed_params,
    }

    if output_path is not None:
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as fh:
            json.dump(report, fh, indent=2, default=str)
        logger.info("EML cross-check report written to %s", output_path)

    return report


def log_cross_check_summary(report: Dict[str, Any]) -> None:
    """Print a human-readable summary of the cross-check report to stdout."""
    s = report["summary"]
    total = s["total_simulations"]
    agreed = s["agreed"]
    failed = s["failed"]
    not_impl = s["not_implemented"]
    errors = s["errors"]
    max_dev = s["global_max_deviation"]

    print("\n" + "=" * 60)
    print("EML MATH CROSS-CHECK SUMMARY")
    print("=" * 60)
    print(f"  Total simulations : {total}")
    print(f"  AGREE             : {agreed}")
    print(f"  FAIL              : {failed}")
    print(f"  NOT_IMPLEMENTED   : {not_impl}")
    print(f"  ERRORS            : {errors}")
    print(f"  Max deviation     : {max_dev:.2e}")

    if s["all_agree"]:
        print(f"\n  [OK] EML CROSS-CHECK: {agreed}/{total} simulations AGREE")
    else:
        print(f"\n  [FAIL] EML CROSS-CHECK: {failed} FAILURES detected")
        fp = report.get("failed_params", {})
        for path, detail in list(fp.items())[:10]:
            print(f"    FAIL  {path}: normal={detail['normal_value']:.6g}  "
                  f"eml={detail['eml_value']:.6g}  "
                  f"dev={detail['deviation']:.2e}")
        if len(fp) > 10:
            print(f"    ... and {len(fp) - 10} more failures")
    print("=" * 60 + "\n")
