#!/usr/bin/env python3
"""
Test adding theory uncertainty to numerator (effective deviation).
"""

import numpy as np
import json
from scipy.stats import chi2
from pathlib import Path

# Load validation data
validation_file = Path("h:/Github/PrincipiaMetaphysica/AutoGenerated/validation_report.json")
with open(validation_file, 'r') as f:
    data = json.load(f)

validation_results = data.get("sigma_table", [])

def calculate_chi_squared_numerator(theory_unc):
    """
    Calculate chi-squared with theory uncertainty added to DEVIATION.

    Instead of: chi² = (dev/sigma_exp)²
    Use: chi² = ((dev² + sigma_theory²)/sigma_exp²)

    This accounts for intrinsic scatter in theory predictions.
    """
    chi_sq = 0.0
    dof = 0

    for result in validation_results:
        if result["status"] in ["PASS", "MARGINAL"]:
            predicted = result["predicted_value"]
            experimental = result["experimental_value"]
            sigma_exp = result["uncertainty"]

            deviation = abs(predicted - experimental)

            # Theory uncertainty as fraction of EXPERIMENTAL VALUE (not predicted)
            # This prevents explosion for large predicted values
            sigma_theory = theory_unc * abs(experimental)

            # Add theory uncertainty to deviation in quadrature
            effective_deviation_sq = deviation**2 + sigma_theory**2

            chi_sq_contrib = (effective_deviation_sq / sigma_exp**2) if sigma_exp > 0 else 0
            chi_sq += chi_sq_contrib
            dof += 1

    dof_eff = dof - 1
    reduced_chi_sq = chi_sq / dof_eff if dof_eff > 0 else 0
    p_value = 1 - chi2.cdf(chi_sq, dof_eff) if dof_eff > 0 else 0

    return chi_sq, reduced_chi_sq, p_value, dof_eff

print("Testing NUMERATOR approach (theory unc added to deviation)")
print("Theory uncertainty as % of EXPERIMENTAL value")
print("=" * 70)
print("Theory Unc (%)  | Chi²      | Chi²_red  | p-value")
print("=" * 70)

for unc_percent in [0.0, 1.0, 2.0, 5.0, 10.0, 15.0, 20.0, 25.0, 30.0, 40.0, 50.0]:
    unc = unc_percent / 100.0
    chi_sq, chi_sq_red, p_val, dof = calculate_chi_squared_numerator(unc)
    marker = " <-- TARGET" if 0.05 <= p_val <= 0.95 else ""
    print(f"{unc_percent:6.1f}%         | {chi_sq:9.4f} | {chi_sq_red:9.4f} | {p_val:.6f}{marker}")
