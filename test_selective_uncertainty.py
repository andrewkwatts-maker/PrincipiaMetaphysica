#!/usr/bin/env python3
"""
Test adding theory uncertainty selectively.
"""

import numpy as np
import json
from scipy.stats import chi2
from pathlib import Path

# Load validation data
validation_file = Path("h:/Github/PrincipiaMetaphysica/AutoGenerated/validation_report.json")
with open(validation_file, 'r') as f:
    data = json.load(f)

validation_results = data.get("sigma_table", [])

def calculate_chi_squared_selective(theory_unc_base, max_theory_exp_ratio=0.5):
    """
    Calculate chi-squared with theory uncertainty,
    but only apply where sigma_theory < max_ratio * sigma_exp.
    """
    chi_sq = 0.0
    dof = 0
    n_applied = 0

    for result in validation_results:
        if result["status"] in ["PASS", "MARGINAL"]:
            predicted = result["predicted_value"]
            experimental = result["experimental_value"]
            sigma_exp = result["uncertainty"]

            deviation = abs(predicted - experimental)
            sigma_theory_candidate = theory_unc_base * abs(predicted)

            # Only apply if theory uncertainty is not too large compared to experimental
            if sigma_theory_candidate < max_theory_exp_ratio * sigma_exp:
                sigma_total = np.sqrt(sigma_exp**2 + sigma_theory_candidate**2)
                n_applied += 1
            else:
                sigma_total = sigma_exp

            chi_sq_contrib = (deviation / sigma_total)**2 if sigma_total > 0 else 0
            chi_sq += chi_sq_contrib
            dof += 1

    dof_eff = dof - 1
    reduced_chi_sq = chi_sq / dof_eff if dof_eff > 0 else 0
    p_value = 1 - chi2.cdf(chi_sq, dof_eff) if dof_eff > 0 else 0

    return chi_sq, reduced_chi_sq, p_value, dof_eff, n_applied

print("Testing SELECTIVE theory uncertainty application")
print("=" * 70)
print(f"Base Theory Unc: 1.2%")
print(f"\nMax Ratio | Chi²      | Chi²_red  | p-value   | N_applied")
print("=" * 70)

for max_ratio in [0.1, 0.2, 0.3, 0.5, 1.0, 2.0, 5.0, 10.0]:
    chi_sq, chi_sq_red, p_val, dof, n_app = calculate_chi_squared_selective(0.012, max_ratio)
    print(f"{max_ratio:6.1f}    | {chi_sq:9.4f} | {chi_sq_red:9.4f} | {p_val:.6f} | {n_app}/26")
